#include <iostream>
#include <limits>
using namespace std;
 
int main() 
{
    int max_int = INT_MAX;
    int min_int = INT_MIN;
    cout << "The maximum value that can be stored in an int variable is: " << max_int << endl; 
    // 2147483647
    cout << "The minimum value that can be stored in an int variable is: " << min_int << endl; 
    // -2147483648
    
    return 0;
}
